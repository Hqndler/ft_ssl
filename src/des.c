int parse_des(des_param *param, char **argv) {
	int i = -1;
	while (argv[++i]) {
		if (!ft_strcmp("-a", argv[i]))
			param->a = 1;
		if (!ft_strcmp("-d", argv[i]))
			param->d = 1;
		if (!ft_strcmp("-e", argv[i]))
			param->e = 1;
		
		if (!ft_strcmp("-i", argv[i])) {
			if (argv[i + 1]) {
				param->fdi = open_file_flag(argv[++i], O_RDONLY, 0);
				if (param->fdi < 0)
					return -1;
			}
			else
				fprint("ft_ssl : Error : \"-i\" option given without value\n");
			continue;
		}
		if (!ft_strcmp("-o", argv[i])) {
			if (argv[i + 1]) {
				param->fdo = open_file_flag(argv[++i], 
											O_CREAT | O_WRONLY | O_TRUNC,
											S_IRWXU | S_IRWXG | S_IRWXO);
				if (param->fdo < 0)
					return -1;
			}
			else
				fprint("ft_ssl : Error : \"-o\" option given without value\n");
			continue;
		}

		if (!ft_strcmp("-k", argv[i])) {
			if (!argv[i + 1])
				fprint("ft_ssl : Error : \"-k\" option given without value\n");
			else
				param->key = argv[i++];
			continue;
		}
		if (!ft_strcmp("-p", argv[i])) {
			if (!argv[i + 1])
				fprint("ft_ssl : Error : \"-p\" option given without value\n");
			else
				param->pass = argv[i++];
			continue;
		}
		if (!ft_strcmp("-s", argv[i])) {
			if (!argv[i + 1])
				fprint("ft_ssl : Error : \"-s\" option given without value\n");
			else
				param->salt = argv[i++];
			continue;
		}
		if (!ft_strcmp("-v", argv[i])) {
			if (!argv[i + 1])
				fprint("ft_ssl : Error : \"-v\" option given without value\n");
			else
				param->vector = argv[i++];
			continue;
		}
	}
	return i;
}

int32_t des(uint8_t **argv, ft_ssl_param p, int argc) {
	(void)argv;
	(void)p;
	(void)argc;

	fprint("Inside DES function\n");

	des_param param = {0};

	parse_des(&param, (char **)argv);

	return 0;
}